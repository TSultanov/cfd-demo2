pub fn generate_dp_init_wgsl(state_stride: u32, d_p_offset: u32) -> String {
    let mut out = String::new();
    out.push_str("// GENERATED BY CFD2 CODEGEN (dp_init)\n\n");
    out.push_str("// DO NOT EDIT MANUALLY\n\n");

    out.push_str(&format!(
        "const STATE_STRIDE: u32 = {state_stride}u;\nconst D_P_OFFSET: u32 = {d_p_offset}u;\n\n"
    ));

    // Match the shared `GpuConstants` layout used by generated kernels so we can bind the
    // common `constants` uniform buffer without a dedicated params struct.
    out.push_str(
        "struct Constants {\n\
    dt: f32,\n\
    dt_old: f32,\n\
    dtau: f32,\n\
    time: f32,\n\
    viscosity: f32,\n\
    density: f32,\n\
    component: u32,\n\
    alpha_p: f32,\n\
    scheme: u32,\n\
    alpha_u: f32,\n\
    stride_x: u32,\n\
    time_scheme: u32,\n\
}\n\n",
    );

    out.push_str("@group(0) @binding(0)\nvar<storage, read> cell_vols: array<f32>;\n\n");
    out.push_str("@group(0) @binding(1)\nvar<storage, read_write> state: array<f32>;\n\n");
    out.push_str("@group(0) @binding(2)\nvar<uniform> constants: Constants;\n\n");

    out.push_str(
        "@compute\n\
@workgroup_size(64)\n\
fn main(@builtin(global_invocation_id) global_id: vec3<u32>) {\n\
    let idx = global_id.y * constants.stride_x + global_id.x;\n\
    let num_cells = arrayLength(&state) / max(STATE_STRIDE, 1u);\n\
    if (idx >= num_cells) {\n\
        return;\n\
    }\n\
\n\
    let rho = max(constants.density, 0.000000000001);\n\
    let dt = max(constants.dt, 0.0);\n\
\n\
    let vol = max(cell_vols[idx], 0.000000000001);\n\
\n\
    // Seed Rhie–Chow mobility with a transient-scale approximation.\n\
    // For integrated momentum diagonals A_U ~ rho*vol/dt (units: kg/s), a good first guess for\n\
    // the mobility-like coefficient is:\n\
    //   d_p ≈ vol / A_U ≈ dt / rho\n\
    //\n\
    // Note: the solver applies velocity under-relaxation in the update stage rather than\n\
    // modifying the assembled momentum diagonal. Match SIMPLE-like behavior by folding the\n\
    // relaxation factor into the mobility: d_p ≈ alpha_u / A_U.\n\
    let d_p = constants.alpha_u * dt / rho;\n\
    state[idx * STATE_STRIDE + D_P_OFFSET] = d_p;\n\
}\n",
    );

    out
}
