fn storage_var(
    name: &str,
    ty: Type,
    group: u32,
    binding: u32,
    access: AccessMode,
) -> Item {
    Item::GlobalVar(GlobalVar::new(
        name,
        ty,
        super::wgsl_ast::StorageClass::Storage,
        Some(access),
        vec![Attribute::Group(group), Attribute::Binding(binding)],
    ))
}

fn uniform_var(name: &str, ty: Type, group: u32, binding: u32) -> Item {
    Item::GlobalVar(GlobalVar::new(
        name,
        ty,
        super::wgsl_ast::StorageClass::Uniform,
        None,
        vec![Attribute::Group(group), Attribute::Binding(binding)],
    ))
}

fn main_fn(
    layout: &StateLayout,
    plan: &MomentumPlan,
    fields: &IncompressibleMomentumFields,
) -> Function {
    let params = vec![Param::new(
        "global_id",
        Type::vec3_u32(),
        vec![Attribute::Builtin("global_invocation_id".to_string())],
    )];
    Function::new(
        "main",
        params,
        None,
        vec![Attribute::Compute, Attribute::WorkgroupSize(64)],
        main_body(layout, plan, fields),
    )
}

fn main_body(
    layout: &StateLayout,
    plan: &MomentumPlan,
    fields: &IncompressibleMomentumFields,
) -> Block {
    let mut stmts = Vec::new();

    stmts.push(dsl::let_(
        "idx",
        "global_id.y * constants.stride_x + global_id.x",
    ));
    stmts.push(dsl::if_block(
        "idx >= arrayLength(&face_areas)",
        dsl::block(vec![Stmt::Return(None)]),
        None,
    ));

    stmts.push(dsl::let_("owner", "face_owner[idx]"));
    stmts.push(dsl::let_("neighbor", "face_neighbor[idx]"));
    stmts.push(dsl::let_("area", "face_areas[idx]"));
    stmts.push(dsl::let_("boundary_type", "face_boundary[idx]"));
    stmts.push(dsl::let_("face_center", "face_centers[idx]"));

    stmts.push(dsl::let_("c_owner", "cell_centers[owner]"));
    stmts.push(dsl::let_typed_expr(
        "c_owner_vec",
        Type::vec2_f32(),
        typed::VecExpr::<2>::from_xy_fields(Expr::ident("c_owner")).expr(),
    ));
    stmts.push(dsl::let_typed_expr(
        "face_center_vec",
        Type::vec2_f32(),
        typed::VecExpr::<2>::from_xy_fields(Expr::ident("face_center")).expr(),
    ));
    stmts.push(dsl::var_typed_expr(
        "normal_vec",
        Type::vec2_f32(),
        Some(
            typed::VecExpr::<2>::from_xy_fields(
                Expr::ident("face_normals").index(Expr::ident("idx")),
            )
            .expr(),
        ),
    ));
    let owner_face_d_vec = typed::VecExpr::<2>::from_expr(Expr::ident("face_center_vec"))
        .sub(&typed::VecExpr::<2>::from_expr(Expr::ident("c_owner_vec")));
    stmts.push(dsl::if_block_expr(
        Expr::binary(
            owner_face_d_vec.dot(&typed::VecExpr::<2>::from_expr(Expr::ident("normal_vec"))),
            BinaryOp::Less,
            Expr::lit_f32(0.0),
        ),
        dsl::block(vec![dsl::assign_expr(
            Expr::ident("normal_vec"),
            typed::VecExpr::<2>::from_expr(Expr::ident("normal_vec")).neg().expr(),
        )]),
        None,
    ));

    let u_field = fields.u.name();
    let p_field = fields.p.name();
    let d_p_field = fields.d_p.name();
    let grad_p_field = fields.grad_p.name();
    let u_owner_expr = state_vec2_expr(layout, "state", "owner", u_field);
    let dp_owner_expr = state_scalar_expr(layout, "state", "owner", d_p_field);
    let grad_p_owner_expr = state_vec2_expr(layout, "state", "owner", grad_p_field);
    let rho_owner_expr = coeff_cell_expr(
        layout,
        plan.ddt.as_ref().and_then(|op| op.coeff.as_ref()),
        "owner",
        "constants.density",
    );
    let rho_face_expr = coeff_face_expr(
        layout,
        plan.ddt.as_ref().and_then(|op| op.coeff.as_ref()),
        "owner",
        "neigh_idx",
        "lambda",
        "rho_face",
    );

    stmts.push(dsl::var("u_face", &u_owner_expr));
    stmts.push(dsl::var("d_p_face", &dp_owner_expr));
    stmts.push(dsl::var("grad_p_avg", &grad_p_owner_expr));
    stmts.push(dsl::var("rho_face", &rho_owner_expr));

    let interior_block = {
        let u_neigh_expr = state_vec2_expr(layout, "state", "neigh_idx", u_field);
        let dp_neigh_expr = state_scalar_expr(layout, "state", "neigh_idx", d_p_field);
        let grad_p_neigh_expr = state_vec2_expr(layout, "state", "neigh_idx", grad_p_field);
        let p_owner_expr = state_scalar_expr(layout, "state", "owner", p_field);
        let p_neigh_expr = state_scalar_expr(layout, "state", "neigh_idx", p_field);

        dsl::block(vec![
            dsl::let_("neigh_idx", "u32(neighbor)"),
            dsl::let_("u_neigh", &u_neigh_expr),
            dsl::let_("d_p_neigh", &dp_neigh_expr),
            dsl::let_("grad_p_neigh", &grad_p_neigh_expr),
            dsl::let_("c_neigh", "cell_centers[neigh_idx]"),
            dsl::let_typed_expr(
                "c_neigh_vec",
                Type::vec2_f32(),
                typed::VecExpr::<2>::from_xy_fields(Expr::ident("c_neigh")).expr(),
            ),
            dsl::let_expr(
                "d_own",
                Expr::call_named(
                    "distance",
                    vec![Expr::ident("c_owner_vec"), Expr::ident("face_center_vec")],
                ),
            ),
            dsl::let_expr(
                "d_neigh",
                Expr::call_named(
                    "distance",
                    vec![Expr::ident("c_neigh_vec"), Expr::ident("face_center_vec")],
                ),
            ),
            dsl::let_("total_dist", "d_own + d_neigh"),
            dsl::var_typed("lambda", Type::F32, Some("0.5")),
            dsl::if_block(
                "total_dist > 1e-6",
                dsl::block(vec![dsl::assign("lambda", "d_neigh / total_dist")]),
                None,
            ),
            dsl::let_expr(
                "lambda_other",
                Expr::binary(Expr::lit_f32(1.0), BinaryOp::Sub, Expr::ident("lambda")),
            ),
            dsl::assign("rho_face", &rho_face_expr),
            dsl::let_typed_expr(
                "u_central",
                Type::vec2_f32(),
                typed::VecExpr::<2>::from_expr(Expr::ident("u_face"))
                    .mul_scalar(Expr::ident("lambda"))
                    .add(
                        &typed::VecExpr::<2>::from_expr(Expr::ident("u_neigh"))
                            .mul_scalar(Expr::ident("lambda_other")),
                    )
                    .expr(),
            ),
            dsl::assign_expr(Expr::ident("u_face"), Expr::ident("u_central")),
            dsl::assign_expr(
                Expr::ident("d_p_face"),
                Expr::binary(
                    Expr::binary(Expr::ident("lambda"), BinaryOp::Mul, Expr::ident("d_p_face")),
                    BinaryOp::Add,
                    Expr::binary(
                        Expr::ident("lambda_other"),
                        BinaryOp::Mul,
                        Expr::ident("d_p_neigh"),
                    ),
                ),
            ),
            dsl::assign_expr(
                Expr::ident("grad_p_avg"),
                typed::VecExpr::<2>::from_expr(Expr::ident("grad_p_avg"))
                    .mul_scalar(Expr::ident("lambda"))
                    .add(
                        &typed::VecExpr::<2>::from_expr(Expr::ident("grad_p_neigh"))
                            .mul_scalar(Expr::ident("lambda_other")),
                    )
                    .expr(),
            ),
            dsl::let_typed_expr(
                "d_vec",
                Type::vec2_f32(),
                typed::VecExpr::<2>::from_expr(Expr::ident("c_neigh_vec"))
                    .sub(&typed::VecExpr::<2>::from_expr(Expr::ident("c_owner_vec")))
                    .expr(),
            ),
            dsl::let_expr(
                "dist_proj",
                Expr::call_named(
                    "abs",
                    vec![typed::VecExpr::<2>::from_expr(Expr::ident("d_vec"))
                        .dot(&typed::VecExpr::<2>::from_expr(Expr::ident("normal_vec")))],
                ),
            ),
            dsl::let_("dist", "max(dist_proj, 1e-6)"),
            dsl::let_("p_own", &p_owner_expr),
            dsl::let_("p_neigh", &p_neigh_expr),
            dsl::let_expr(
                "grad_p_n",
                typed::VecExpr::<2>::from_expr(Expr::ident("grad_p_avg"))
                    .dot(&typed::VecExpr::<2>::from_expr(Expr::ident("normal_vec"))),
            ),
            dsl::let_("p_grad_f", "(p_neigh - p_own) / dist"),
            dsl::let_("rc_term", "d_p_face * area * (grad_p_n - p_grad_f)"),
            dsl::let_expr(
                "u_n",
                typed::VecExpr::<2>::from_expr(Expr::ident("u_face"))
                    .dot(&typed::VecExpr::<2>::from_expr(Expr::ident("normal_vec"))),
            ),
            dsl::assign_expr(
                Expr::ident("fluxes").index(Expr::ident("idx")),
                Expr::binary(
                    Expr::ident("rho_face"),
                    BinaryOp::Mul,
                    Expr::binary(
                        Expr::binary(Expr::ident("u_n"), BinaryOp::Mul, Expr::ident("area")),
                        BinaryOp::Add,
                        Expr::ident("rc_term"),
                    ),
                ),
            ),
        ])
    };

    let inlet_block = dsl::block(vec![
        dsl::let_(
            "ramp",
            "smoothstep(0.0, constants.ramp_time, constants.time)",
        ),
        dsl::let_typed_expr(
            "u_bc",
            Type::vec2_f32(),
            typed::VecExpr::<2>::from_components([
                Expr::binary(
                    Expr::ident("constants").field("inlet_velocity"),
                    BinaryOp::Mul,
                    Expr::ident("ramp"),
                ),
                Expr::lit_f32(0.0),
            ])
            .expr(),
        ),
        dsl::assign_expr(
            Expr::ident("fluxes").index(Expr::ident("idx")),
            Expr::binary(
                Expr::binary(
                    Expr::ident("rho_face"),
                    BinaryOp::Mul,
                    Expr::call_named("dot", vec![Expr::ident("u_bc"), Expr::ident("normal_vec")]),
                ),
                BinaryOp::Mul,
                Expr::ident("area"),
            ),
        ),
    ]);

    let wall_block = dsl::block(vec![dsl::assign_expr(
        Expr::ident("fluxes").index(Expr::ident("idx")),
        Expr::lit_f32(0.0),
    )]);

    let outlet_block = dsl::block(vec![
        dsl::let_expr(
            "u_n",
            typed::VecExpr::<2>::from_expr(Expr::ident("u_face"))
                .dot(&typed::VecExpr::<2>::from_expr(Expr::ident("normal_vec"))),
        ),
        dsl::var_typed("rc_term", Type::F32, Some("0.0")),
        dsl::let_expr(
            "dist_face",
            Expr::call_named(
                "distance",
                vec![Expr::ident("c_owner_vec"), Expr::ident("face_center_vec")],
            ),
        ),
        dsl::if_block(
            "dist_face > 1e-6",
            dsl::block(vec![dsl::assign("rc_term", "0.0")]),
            None,
        ),
        dsl::let_("raw_flux", "rho_face * (u_n * area + rc_term)"),
        dsl::assign_expr(
            Expr::ident("fluxes").index(Expr::ident("idx")),
            Expr::call_named("max", vec![Expr::lit_f32(0.0), Expr::ident("raw_flux")]),
        ),
    ]);

    let boundary_block = dsl::block(vec![dsl::if_block(
        "boundary_type == 1u",
        inlet_block,
        Some(dsl::block(vec![dsl::if_block(
            "boundary_type == 3u",
            wall_block,
            Some(dsl::block(vec![dsl::if_block(
                "boundary_type == 2u",
                outlet_block,
                Some(dsl::block(vec![dsl::assign("fluxes[idx]", "0.0")])),
            )])),
        )])),
    )]);

    stmts.push(dsl::if_block(
        "neighbor != -1",
        interior_block,
        Some(boundary_block),
    ));

    Block::new(stmts)
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::solver::codegen::lower_system;
    use crate::solver::model::incompressible_momentum_model;
    use crate::solver::model::backend::SchemeRegistry;
    use crate::solver::scheme::Scheme;

    #[test]
    fn flux_rhie_chow_codegen_emits_state_arrays() {
        let model = incompressible_momentum_model();
        let schemes = SchemeRegistry::new(Scheme::Upwind);
        let discrete = lower_system(&model.system, &schemes).unwrap();
        let fields = model.fields.incompressible().expect("incompressible fields");
        let wgsl = generate_flux_rhie_chow_wgsl(&discrete, &model.state_layout, fields);
        assert!(wgsl.contains("state: array<f32>"));
        assert!(wgsl.contains("face_owner"));
        assert!(wgsl.contains("smoothstep"));
    }
}
